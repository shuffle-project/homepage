<p>
	In diesem Jahr lautete der Schwerpunkt des Tages „Nachhaltig(-keiten) lehren und lernen – Lehre
	zukunftsfähig gestalten“. So wurde am 23. November in hybrider Form fakultätsintern sowie -extern
	über Nachhaltigkeit referiert und diskutiert. Darüber hinaus hatten aktuell an der Universität
	Bielefeld geförderte Lehr-Forschungsprojekte die Gelegenheit, ihr Vorhaben in einer Postersession
	vorzustellen. Diese Möglichkeit nutzte auch das Bielefelder Team von SHUFFLE. Neben der
	Präsentation der Gesamtvorhabens wurde der Austausch mit Bielefelder Lehrenden genutzt, um auf die
	Erprobung der in SHUFFLE erstellten Materialien und Tools aufmerksam zu machen.
</p>

