<p>
	Vergangene Woche war ein Teil des AP06-Teams auf dem 37. VBS Kongress in Marburg (Verband für
	Blinden- und Sehbehindertenpädagogik).
</p>

<p>
	In interessanten Vorträgen, Workshops, Hilfsmittelausstellungen und Selbsterfahrungstrainings
	konnten Ann-Katrin, Niklas, Frank, Markus und Patricia in die aktuellen Diskurse der Blinden- und
	Sehbehindertenpädagogik eintauchen. Trotz oder vielleicht sogar dank des durchgehend schlechten
	Wetters war der Kongress mit fast 700 Teilnehmenden sehr gut besucht.
</p>

<p>
	Nachdem das Team unter der Woche schon fleißig die visuell und taktil gestalteten Flyer zur
	Persona „Gabriel“ verteilt hatte, war es dann am Donnerstagnachmittag endlich soweit: BlindDate
	und Gabriel gehen live! Die Aula der Blista (Blindenstudienanstalt in Marburg) war mit rund 50
	Interessierten gefüllt, dem Vortrag zu BlindDate wurde gespannt gelauscht und anschließend einige
	Fragen beantwortet.
</p>

<p>
	Gabriel ist die erste Persona der Website, die nun besucht werden kann und er repräsentiert
	Studierende mit eine Sehbeeinträchtigung. BlindDate befindet sich nach wie vor im Aufbau und
	weitere Persona mit anderen Beeinträchtigungen werden folgen – Rückmeldungen, sowohl zur
	technischen als auch zur inhaltlichen Umsetzung, sind daher herzlich willkommen und können im
	Anschluss an den Besuch der Gabriel-Seite in einer direkt angeschlossenen Umfrage gegeben werden.
</p>

