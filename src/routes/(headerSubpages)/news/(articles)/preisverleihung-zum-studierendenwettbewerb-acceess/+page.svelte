/>

<p>
	Die Informatikstudierenden hatten im Sommersemester 2022 jeweils Prototypen einer Prüfplattform
	entwickelt, mit der Lehrende ihre digitalen Lernräume auf Barrierefreiheit hin überprüfen können.
	Ein Mischung aus automatisierten und manuellen Überprüfungsmöglichkeiten gibt Lehrenden sowohl in
	Moodle als auch in ILIAS Feedback zur Barrierefreiheit ihrer jeweiligen Lernräume und zeigt
	Lösungen auf, um digitale Barrieren zu beseitigen. Gamification-Ansätze erweitern die
	Nutzerfreundlichkeit für Lehrende.
</p>

<p>
	Die Gewinnerteams konnten sich über Geldprämien im Wert von bis zu 5.000€ freuen und beim
	anschließenden Get together mit Kommiliton*innen, Jury und Gästen auf ihren Erfolg anstoßen.
</p>
