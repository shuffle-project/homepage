<p>
	Am 28. September 2022 fanden hybrid die Abschlusspräsentation und Preisverleihung zum
	Studierendenprojekt ACCESS statt. Aus zehn Teilnehmerteams wurden von der Jury drei Gewinnerteams
	ausgewählt und ihre Entwicklungstools prämiert.
</p>

<img
	src="{base}/articles/preisverleihung-zum-studierendenwettbewerb-acceess/shuffle-acceess22.jpg"
	alt=""
