<p>
	Ende März fand unser Frühlingstreffen im sonnigen Freiburg statt. Nachdem wir im Februar die
	Zusage der Verlängerung des Projekts bis Ende Dezember 2025 bekommen haben, nutzten wir das
	Präsenztreffen für eine Bestandsaufnahme und Koordination der weiteren Aufgaben. So haben wir uns
	am Donnerstag zunächst einmal gegenseitig über die aktuellen Projektstände in den einzelnen
	Teilprojekten informiert. Das hat super geklappt und war sowohl informativ als auch ein lockerer
	Start in das Präsenztreffen!
</p>

<p>
	Am Mittag widmeten wir uns dann der Vorbereitung von Zwischen- und Abschlussberichten, den
	geplanten Veröffentlichungen und Konferenzteilnahmen und -Beiträgen. Diesen erfolgreichen Tag
	konnten wir dann mit einem Spaziergang auf den Schlossberg, Führung durch das wunderschöne
	Freiburg bei sommerlichen Temperaturen und leckerem Abendessen in der „Ersten Liebe“ abschließen.
</p>

<p>
	Am Freitag wurden die Überlegungen des Vortages und wichtige Termine direkt festgeklopft. Außerdem
	konnten wir inhaltlich in den Arbeitspaketen weiterarbeiten. Den Abschluss des Frühlingstreffens
	bildete dann eine sehr spannende Diskussionsrunde auf der sonnigen Dachterrasse über Ideale und
	Ansprüche unserer Arbeit.
</p>

<p>
	Neben der inhaltlichen Arbeit hatten wir an diesen beiden Tagen mal wieder Zeit, uns persönlich
	auszutauschen, zu diskutieren und festzustellen, dass wir uns sehr freuen, die nächsten 1 ½ Jahre
	als Team zusammenzuarbeiten!
</p>

