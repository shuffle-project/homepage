<p>
	Nach mehr als vier Jahren intensiver Zusammenarbeit fand das Projekt SHUFFLE am 31. Dezember
	seinen formalen Abschluss. Für die Beteiligten bedeutet das mehr als das Ende eines
	Förderzeitraums, denn es ist zugleich der Abschluss einer besonderen gemeinsamen Zeit.
</p>

<p>
	Von August 2021 bis Dezember 2025 haben vier Partnerhochschulen im Projekt SHUFFLE gemeinsam daran
	gearbeitet, die digitale Barrierefreiheit an Hochschulen praktisch und nachhaltig zu verbessern.
	Mit insgesamt mehr als 40 Beteiligten aus unterschiedlichen Disziplinen war das kein kurzfristiges
	Vorhaben, sondern ein langfristiger gemeinsamer Entwicklungsprozess.
</p>

<p>
	Ausgangspunkt des Projekts war der Anspruch, chancengerechte digitale Teilhabe für alle
	Studierenden nicht nur als rechtliche Verpflichtung, sondern als gestaltbare Aufgabe im
	Hochschulalltag zu verstehen. SHUFFLE setzte deshalb bewusst auf einen ganzheitlichen Ansatz:
	Maßnahmen zur digitalen Barrierefreiheit wurden auf struktureller, didaktischer und technischer
	Ebene entwickelt, erprobt und miteinander verzahnt. Lehrende und Studierende wurden aktiv in die
	Entwicklungen einbezogen, sodass Lösungen nicht nur für, sondern mit den Nutzerinnen und Nutzern
	entstanden sind.
</p>

<p>
	Im Laufe des Projekts sind zahlreiche konkrete Ergebnisse entstanden, die Hochschulen dabei
	helfen, digitale Barrierefreiheit umzusetzen:
</p>

<p>
	Dazu zählt ein Reifegradmodell, das Hochschulen bei der systematischen Analyse und
	Weiterentwicklung ihrer digitalen Barrierefreiheit unterstützt. Mit <span lang="en"
		>BlindDate</span
	>
	wurde eine Begegnungsplattform geschaffen, auf der virtuelle Studierende mit Beeinträchtigungen Einblicke
	in ihren Studienalltag geben und so einen Perspektivwechsel ermöglichen. Mit MELVIN entstand ein einfach
	nutzbares Tool zum Erstellen und Teilen untertitelter Videos für den Lehralltag. Umfangreiche Ressourcen
	und Hilfestellungen zur barrierefreien Gestaltung der Lernmanagementsystemen Moodle und ILIAS, von
	Präsentationen, Dokumenten und digitalen Prüfungen wurden zusammengetragen. Ergänzt werden diese Angebote
	durch ein <span lang="en">Badge</span>, das erworbenes Wissen im Bereich digitaler
	Barrierefreiheit in der Lehre zertifiziert.
</p>

<p>
	Für alle Beteiligten war SHUFFLE mehr als die Summe seiner Ergebnisse. Das Projekt bot uns den
	Freiraum, Produkte mit wissenschaftlichem Anspruch, aber auch viel mit Raum für Kreativität von
	Grund auf zu gestalten – von den ersten Konzepten über die Umsetzung bis hin zur Pilotierung. Ob
	bei Literaturrecherchen, dem Durchführen von Interviews, dem Schreiben und Vortragen
	wissenschaftlicher Publikationen, beim Programmieren oder Illustrieren – jeder konnte seine
	persönlichen Stärken einbringen. Die interdisziplinäre Zusammenarbeit über Hochschulgrenzen hinweg
	war dabei stets wertschätzend und auf Augenhöhe. SHUFFLE zu verabschieden fällt uns deshalb nicht
	leicht und wir werden diese besondere Form der Zusammenarbeit sehr vermissen.
</p>

<p>
	Die entwickelten Anwendungen und Materialien stehen auch über das Projektende hinaus <span
		lang="en">open source</span
	>
	zur Verfügung und können weiter genutzt, angepasst und weiterentwickelt werden. Denn mit dem Abschluss
	von SHUFFLE endet ein <em>Projekt</em>, die Arbeit an digitaler Barrierefreiheit an Hochschulen
	jedoch nicht.
</p>

<p>
