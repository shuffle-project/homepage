<p>
	SHUFFLE ist präsent! Dieses Mal in Form einer Posterpräsentation auf der Herbsttagung der Sektion
	Medienpädagogik der Deutschen Gesellschaft für Erziehungswissenschaft (DGfE), die im September
	2022 an der Universität Bielefeld stattfand.
</p>

<p>
	Im Fokus unseres Posters standen die Ergebnisse der Lehrendenbefragung unserer empirischen Daten
	aus AP01. Wir beschäftigten uns mit den Fragen, was Lehrende über eine digitale Lehrgestaltung für
	Alle wissen und welche Erfahrungen und Einstellungen sie gegenüber zugänglicher digitaler Lehre
	mitbringen. Durch reges Interesse der Teilnehmenden konnten wir einige interessante Diskussionen
	führen und mit anderen Wissenschaftler*innen ins Gespräch kommen. In den nächsten Wochen treten
	wir sicherlich mit dem ein oder der anderen nochmal in Kontakt.
</p>

<p>
	Wir freuen uns sehr, dass das Thema der digitalen Barrierefreiheit in der Lehre an deutschen
	Hochschulen auf so offene Ohren stößt!
</p>

