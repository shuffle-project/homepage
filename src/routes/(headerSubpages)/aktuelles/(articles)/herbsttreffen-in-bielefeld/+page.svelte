<p>
	Zum zweiten Mal trafen sich die Projektmitglieder am Standort Bielefeld. Dieses Mal fand das
	Treffen vom 23.-24. September in einem inklusiven Hotel im Stadtteil Bethel statt. Es galt
	insofern nicht nur über Inklusion zu sprechen, sondern sie auch ganz konkret zu erleben. Aus
	inhaltlicher Sicht stand die Planung der Projektverlängerungsphase mit Fertigstellung der Produkte
	sowie der Transfer und die Verbreitung der Projektergebnisse im Vordergrund. Für das Jahr 2025
	wurden zahlreiche Publikations- und Tagungsaktivitäten geplant. Nach dem Feierabend stand dann
	auch der Besuch der historischen Sammlung Bethel - das Museum zur Geschichte der v.
	Bodelschwinghschen Stiftungen - auf dem Programm.
</p>

<p>
	Wir blicken auf ein erfolgreiches Treffen zurück, in dem wir gemeinsam die Weichen für ein
	erfolgreiches letztes gemeinsames Jahr im Projekt stellen konnten.
</p>

