<p>
	Am 14. und 15. November 2024 war ein Teil des SHUFFLE-Teams auf der TURN Conference in Berlin
	dabei – und das nicht nur als Teilnehmende! Wir hatten die Gelegenheit, unsere Projekte
	vorzustellen und uns mit vielen spannenden Akteur*innen aus Wissenschaft und Praxis auszutauschen.
	Die Konferenz fand unter dem Motto „Horizonte verbinden“ im beeindruckenden Henry-Ford-Bau der
	Freien Universität statt und brachte rund 500 Menschen aus den unterschiedlichsten Bereichen
	zusammen.
</p>

<p>
	Mit dabei waren zwei unserer Projekte: BlindDate und unsere Moodle- und ILIAS-Lernräume zu
	Barrierefreiheit. Es war eine großartige Möglichkeit, unsere Arbeit einem breiten Publikum
	vorzustellen und ganz konkret zu zeigen, wie für Barrierefreiheit in der Lehre sensibilisiert und
	diese umgesetzt werden kann.
</p>

<p>
	Die Resonanz auf unsere Präsentationen war positiv und es entstanden spannende Gespräche, in denen
	deutlich wurde, wie wichtig solche Projekte für eine inklusive Hochschullandschaft sind. Neben den
	Präsentationen war die TURN Conference auch eine perfekte Gelegenheit, neue Kontakte zu knüpfen
	und unser Netzwerk zu erweitern. Wir nehmen jede Menge Ideen und Denkanstöße mit, die uns in
	unserer Arbeit weiterbringen.
</p>

<p>
	Insgesamt blicken wir auf zwei intensive, inspirierende Tage zurück, die gezeigt haben, wie viel
	Potenzial in der Verbindung von Theorie, Praxis und Austausch steckt. Wir freuen uns darauf, die
	Impulse der TURN Conference in unsere weiteren Schritte mitzunehmen und gemeinsam weiter an einer
	barrierefreien Hochschullandschaft zu arbeiten!
</p>

