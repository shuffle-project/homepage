<p>
	Ende Februar haben wir uns für zwei Tage zu einem intensiven und aufschlussreichen Team-Meeting in
	Bielefeld an der Universität getroffen. Viele Team-Mitglieder hatten interessante Sessions
	vorbereitet und wir konnten uns über verschiedenste Themen austauschen und diskutieren.
</p>

<p>
	Den ersten Vormittag haben wir damit verbracht, sowohl unsere Rollen als Einzelpersonen im Team zu
	identifizieren als auch den aktuellen Stand des Projekts. Da wir momentan Halbzeit haben, uns in
	einer Fußball-Metapher also in der 47. Spielminute befinden, haben wir unseren Fokus auf drei
	entscheidende Dinge gelegt: 1. Grundlagentraining (Passgenauigkeit und Team-Absprachen), 2.
	Spielanalyse (Sind wir gut aufgestellt?) und 3. Tore schießen (Was sind unsere Ziele und
	Torchancen?). So konnten wir die Perspektiven der vergangenen 1,5 Jahre und der noch kommenden 1,5
	Jahre vereint betrachten.
</p>

<p>
	Am Nachmittag hatten wir die Möglichkeit, für das Arbeitspaket 2 Strategien und Prozesse genauer
	unter die Lupe zu nehmen. Durch Brainstorming und Input aus dem AP haben wir so
	Handlungsempfehlungen für die Bereiche Sensibilisierung, Kommunikation, Ansprechpersonen &
	Anlaufstellen, Struktur & Strategie, Transfer & Netzwerke sowie Infrastruktur an Hochschulen
	entwickelt.
</p>

<p>
	Den zweiten Vormittag konnten wir dann für die Arbeit in den einzelnen Arbeitspaketen nutzen, um
	diese in Präsenz weiter voranzutreiben. Zum Abschluss bekam dann das gesamte SHUFFLE-Team noch
	eine Führung und einen Einblick in die Tätigkeiten der ZAB. Wir durften die dort vorhandenen
	Barrierefreiheitstools testen und Simulationen ausprobieren. Mit neuer Motivation und vielen
	Plänen sind wir am Dienstag dann wieder nach Hause gefahren und voller Energie in die nächste
	Phase des Projekts gestartet.
</p>

